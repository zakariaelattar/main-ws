const express = require('express');
const rootRoute = require('./products.route.js');
const productIdRoutes = require('./productId');

const router = express.Router({ mergeParams: true });

const defaultRoutes = [{
        path: '/',
        route: rootRoute
    },
    {
        path: '/:productId',
        route: productIdRoutes
    }

];


// All products can access those routes, the only condition is to be authenticated
defaultRoutes.forEach((route) => {
    router.use(route.path, route.route);
});



module.exports = router;



